<h2>2010 Liver Life Walk</h2>

<p>On  June 6, 2010, I will participate in the Liver Life Walk to raise funds for the work of the American Liver Foundation – the nation’s leading nonprofit organization dedicated to liver health and disease prevention. Our team goal is to raise more than <i>$750</i> for this worthy cause.</p>

<p>Liver disease is often unknown or misunderstood. Here are some compelling statistics:</p>
<ul>
<li>1 out of every 10 Americans is affected by about 100 types of liver disease.</li>
<li>That is 30 million people — the number of people in the United States living with diabetes and cancer combined, and the same number of people who live in America’s 15 largest cities. </li>
<li>60% of the 26,000 people who die each year from cirrhosis have no history of alcohol abuse. </li>
<li>Nearly 17,000 people are on the waiting list for a liver transplant. </li>
</ul>

<p>The American Liver Foundation (ALF) is dedicated to a future where there are effective treatments, cures, and no one dies waiting for a liver transplant.  The ALF mission is to facilitate, advocate and promote education, support and research for the prevention, treatment and cure of liver disease.</p>

