</ul>

<p>If you would like to do more, you can also join our team and set a fundraising goal for yourself. You can register today by visiting <a href="http://www.liverfoundation.org/walk">www.liverfoundation.org/walk</a> and following the prompts to register for the New York City Liver Life Walk. When you register, you will receive valuable fundraising tools and information from the American Liver Foundation, in addition to your own personal fundraising Web site. </p>

<p>With your help, we will fund important work and raise awareness of these diseases that afflict millions of people. If you have questions about the Liver Life Walk or the work of the American Liver Foundation, please do not hesitate to contact me. Thank you in advance for your support! </p>

