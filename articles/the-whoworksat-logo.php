<h2>The WhoWorks.At Logo</h2>

<p>It was Saturday, April 17th 2011, late at night and I get a call from <a href="https://twitter.com/johndbritton">John Britton</a>. He was super pumped about this awesome idea he just had called <a href="http://whoworks.at/">WhoWorks.At</a>. He tells me all about this idea and I loved it. It is simple and people can actually use it. He then asked me if I would be willing to quickly make him a logo for the landing page he was building. I asked him what he wanted and he said "oh, I have it sketched out on a napkin". I tell him to send it over and I will see what I can do.</p>
<p>I get an email a few minutes later at 1:10am with image of his sketch and a short note from him "Looks like an @ symbol, but with a W, slightly tilted to vaguely resemble the filament of a light bulb".</p>
<img src="/articles/assets/the-whoworksat-logo/sketch.png">
<p>By 1:45am I replied to him with a finished logo just as he requested. I got an immediate response back from John "Dude, this is absolutely perfect. Thanks so much, I love it."</p>
<img src="/articles/assets/the-whoworksat-logo/logo.png">
<p>And this is how the WhoWorks.At logo was born. It is not the most elaborate logo, nor did we spend that much time on it. Sometimes you just know what you want and more often then not simple is just better. Just another night in the startup world.</p>

<p class="byline">January 4th, 2013 &there4; Kennedy</p>

